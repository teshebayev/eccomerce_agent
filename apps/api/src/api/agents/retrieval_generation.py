@traceable(
    name="rag_pipeline",
    run_type="chain",
)
def rag_pipeline(query, qdrant_client, top_k=5):
    retrieved_data = retrieve_data(query, qdrant_client, top_k)
    processed_context = process_context(retrieved_data)
    prompt = build_prompt(query, processed_context)
    answer = generate_answer(prompt)

    final_result = {
        "answer": answer.answer,
        "references": answer.references,
        "question": query,
        "retrieved_context": retrieved_data["retrieved_contexts"],
        "retrieved_context_ids": retrieved_data["retrieved_context_ids"],
        "similarity_scores": retrieved_data["similarity_scores"],
    }

    return final_result



def rag_pipeline_wrapper(question, top_k=5):
    qdrant_client = QdrantClient(
        url="http://qdrant:6333",
    )

    result = rag_pipeline(question, qdrant_client, top_k)
    used_context = []
    dummy_vector = numpy.zeros(768).tolist() 
    for item in result.get("references", []):
        payload = qdrant_client.query_points(
            collection_name="Amazon_items_collection1_hybrid_search",
            query=dummy_vector,
            with_payload=True,
            using ="nomic-embed-text",
            limit=1,
            query_filter= Filter(
                must =  [
                    FieldCondition(
                        key="parent_asin",
                        match= MatchValue(value=item.id)
                        )
                    ])
        ).points[0].payload
        image_url = payload.get("image")
        price = payload.get("price")
        if image_url:
            used_context.append({
                "image_url": image_url,
                "price": price,
                "description": item.description
            })

    return {
        "answer": result["answer"],
        "used_context": used_context,
    }


